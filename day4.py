## day4 - Basic Operator and Expressions

num1 = 10
num2 = 3
print("\n--- Arithmwtic Operations ---")
print("Addition (10+3):", num1 + num2)
print("Subtraction (10-3):", num1 - num2)
print("Multiplication (10*3):", num1 * num2)
print("Division (10/3):", num1 / num2)
print("Floor Division (10//3)", num1 // num2)
print("Exponentiation (10**3):", num1 ** num2)

print("\n--- Relational Operations ---")
print("Is 10 equal to 3? ", num1 == num2)
print("Is 10 not equal to 3? ", num1 != num2)
print("Is 10 greater than 3? ", num1 > num2)
print("Is 10 less than 3? ", num1 < num2)
print("Is 10 greater than or equal to 3? ", num1 >= num2)
print("Is 10 less than or equal to 3? ", num1 <= num2)

